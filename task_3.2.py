#решение задачи через словарь
seasons = {12: "Зима", 1: "Зима", 2: "Зима", 3: "Весна", 4: "Весна", 5: "Весна",
           6: "Лето", 7: "Лето", 8: "Лето", 9: "Осень", 10: "Осень", 11: "Осень"}
month = 0
while month < 1 or month > 12:
    month = int(input("Для определения времени года, введите месяц в виде целого числа от 1 до 12: "))

for i in seasons:
    if month == i:
        print(f"{month}-ый месяц соответствует времени года: {(seasons.get(i))}")
